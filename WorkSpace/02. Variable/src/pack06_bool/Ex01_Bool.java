package pack06_bool;

public class Ex01_Bool { 
	public static void main(String[] args) {
	// 부울형(논리형): boolean
	// true, false만 기억 -> 조건식 작성 시 사용
	// 컴퓨터는 어떤 판단을 true와 false만 함
	// 컴퓨터에게 질문할 때는 true, false 답만 나오는 것만 가능
	// 예시) 내 이름이 뭐게? (X)
	// 오늘 온도가 높은지? (X)
	// 오늘 온도가 15도보다 높습니까? (O)
	boolean t = true;
	boolean f = false;
	// 문자열도 변수도 표현하고 싶다면 + 사용
	System.out.println("변수 t에 들어 있는 값: " + t);
	System.out.println("변수 f에 들어 있는 값: " + f);
	
	
}

}
