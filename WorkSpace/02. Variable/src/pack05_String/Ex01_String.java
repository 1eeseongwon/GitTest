package pack05_String;

public class Ex01_String { 
	public static void main(String[] args) {
		// Java에서 제일 많이 사용되는 데이터타입
		// int(정수), double(실수), String(문자열)
		// " == 쌍따옴표 == 더블 코테이션 
		// 데이터를 할당할 때는 더블 코테이션으로 내용을 감싸서 할당
		// String 타입의 변수 str에 아무 문자열 넣고 초기화 
		String str = "이성원"; //초기화식
		System.out.println(str);
		System.out.println(); // println() 사이에는 하나의 변수
		// 또는 변수에 담을 수 있는 값
		int a = 1;
		int b = 2;
		System.out.println(a + b);
	}

}
