package pack07_escape;

public class Ex01_Escape {
	public static void main(String[] args) {
		//문자열 사이에 어떠한 기호를 넣어서 띄어쓰기나 줄바꿈
		// 특수문자 표현을 하기 위한 문자
		System.out.println(" 이름 : \"이성원\" ");
		System.out.println("번호\t이름\t직업"); // \t 탭(공백)
		System.out.print("문자1\n"); // \n 줄바꿈 
		System.out.print("문자2");
		System.out.println("d:\\study_java"); // \ 쓰고 싶으면 *2
		
	}

}
