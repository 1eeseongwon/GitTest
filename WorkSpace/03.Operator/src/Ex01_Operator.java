
public class Ex01_Operator {
	public static void main(String[] args) {
		int X = -100;
		int result1 = +X; // 부호 +를 붙임. 
		// result1의 결과는 어떤 수일까
		int result2 = -X; // 부호 -를 붙임.
		System.out.println("(+X) result1 = " + result1);
		System.out.println("(-X) result2 = " + result2);
		System.out.println("(-X) X*-1" + X + -1);
		// -부호는 부호를 반전시키는 규칙이 있음 (+는 규칙 X)
		
		
		
		}

}
