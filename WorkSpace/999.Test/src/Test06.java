
public class Test06 {
	public static void main(String[] args) {
		int korScore, mathScore, engScroe, sciScore, peScore;
		korScore = 90;
		mathScore = 90;
		engScroe = 90;
		sciScore = 90;
		peScore = 90;
		// 네이밍룰: Class 파일은 대문자로 시작
		// 변수는 소문자로 시작하고, 의미 있는 단어 연결해서 조합
		System.out.println(korScore);
		
		// 형 변환(Casting)
		// 서로 다른 타입의 변수를 변환시켜 사용함
		// ex) int <-> double, String <-> int
		// int < double
		int iValue = 10;
		double dValue = iValue + 0.5; // 자동 형 변환
		System.out.println("iValue 값 : " + iValue);
		System.out.println("dValue 값 : " + dValue);
		
		// 더 큰 데이터 타입을 작은 타입에 담을 때 오류 발생
		// 어떤 타입으로 바꿀 건지 명시를 해 줘서 강제로 바꿈
		// 강제 형 변환
		iValue = (int) dValue; // 소숫점 유실
		System.out.println("iValue 값 : " + iValue);
		System.out.println("dValue 값 : " + dValue);
		
		String str = "1234" + 12; // 답: 123412
		// 문자열에 어떤 값을 더하는 것 > 결합 (글자의 합침)
		int iData = 1234 + 12; // 답: 1246
		// 숫자형 데이터 합은 > 일반적인 합 (더하기)
		System.out.println(iData);
		System.out.println(str);
		// str에는 현재 123412라는 문자열이 저장되어 있음
		// 숫자로 바꾸려면 어떻게
		int sum1 = Integer.parseInt(str)+1;
		System.out.println(sum1);
		
		// boolean -> true, false만 저장 가능
		
		String sum2 = ""+1234+5678+"가나다라";
		
	}
}
